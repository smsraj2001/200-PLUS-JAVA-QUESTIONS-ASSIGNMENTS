/**Perform different Arithmetic operations using command line printing.*/

public class EXAMPLE_31 
{
public static void main(int num1,int num2)
{
int result;
result=num1+num2;
System.out.println(num1+" + "+num2+" = "+result);
result=num1-num2;
System.out.println(num1+" - "+num2+" = "+result);
result=num1*num2;
System.out.println(num1+" * "+num2+" = "+result);
result=num1/num2;
System.out.println(num1+" / "+num2+" = "+result);
result=num1%num2;
System.out.println(num1+" % "+num2+" = "+result);
}
}
    