/**An athlete takes 18 rounds of a triangular park with sides as 30m,25m and 35m.Write a program to determine
the total distance covered by him.*/

public class PROGRAM12
{
public static void main(String[]args)
{
int L1,L2,L3,T_DIST;
L1=30;
L2=25;
L3=35;
T_DIST=18*(L1+L2+L3);
System.out.println("The total distance covered by the athlete is "+T_DIST+"m.");
}
} 
